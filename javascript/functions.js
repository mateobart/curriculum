document.getElementById("contacto_boton").addEventListener("click", function(){
    if(document.getElementById("contacto_box").style.display === "none"){
        document.getElementById("contacto_box").style.display = "block";
    } else {
        document.getElementById("contacto_box").style.display = "none";
    }
})

